let currentLang = 'es';

export function t(key) {
  return translations[currentLang]?.[key] ?? translations.es[key] ?? key;
}

export function getLanguage() {
  return currentLang;
}

export function applyStaticTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

export function setLanguage(lang) {
  if (lang !== 'es' && lang !== 'en') return;
  currentLang = lang;
  document.documentElement.lang = lang;
  try { localStorage.setItem('lmsLanguage', lang); } catch {}
  applyStaticTranslations();
}

// Llamar en DOMContentLoaded (antes de renderizar contenido dinámico) — el
// script inline en <head> ya aplicó el <html lang> desde localStorage para
// evitar FOUC; esto sincroniza el módulo con ese mismo valor y traduce el
// HTML estático ya presente en el DOM.
export function initI18n() {
  let cached = null;
  try { cached = localStorage.getItem('lmsLanguage'); } catch {}
  if (cached === 'es' || cached === 'en') currentLang = cached;
  document.documentElement.lang = currentLang;
  applyStaticTranslations();
}
